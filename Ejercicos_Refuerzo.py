# Ejercicio 2: 
# Registro de Alturas y Clasificación Desarrolla una función registro_alturas() 
# que permita ingresar la altura de 10 personas en centímetros.
# Para cada persona, el programa debe solicitar la altura ingresada por el usuario y 
# luego clasificarla en uno de los siguientes grupos:
    # Baja (menos de 160 cm)
    # Media (entre 160 cm y 180 cm, inclusive)
    # Alta (más de 180 cm)
# Al final, el programa debe mostrar:
# Cuántas personas pertenecen a cada categoría.
# La altura promedio de todos los encuestados.

